/// Thanks to build.rs.
const OUT_DIR: &str = env!("OUT_DIR");

static RND_PART: LazyLock<String> = LazyLock::new(|| {
    file::existing_rnd_part(OUT_DIR)
        .unwrap()
        .expect("Expecting a file generated by build.rs.")
});

fn var_or_const_name(given_name: Ident) -> Ident {
    var_or_const_name_internal(None, given_name)
}

fn var_or_const_name_prefixed_with_path(macro_crate_and_path: Path, given_name: Ident) -> Ident {
    let path_based_prefix = macro_crate_and_path.into_token_stream().to_string();
    let path_based_prefix = path_based_prefix.replace("::", "_"); // this creates a new String - that's OK.
    var_or_const_name_internal(Some(path_based_prefix), given_name)
}

fn var_or_const_name_internal(path_based_prefix: Option<String>, given_name: Ident) -> Ident {
    let span = given_name.span();

    let rnd_part = &*RND_PART;
    let full_name = if let Some(path_based_prefix) = path_based_prefix {
        // Intentionally putting the random part at the end, to make errors/warnings more
        // obvious/searchable.
        format!("{path_based_prefix}_private_{given_name}_{rnd_part}")
    } else {
        format!("private_{given_name}_{rnd_part}")
    };
    Ident::new(&full_name, span)
}

